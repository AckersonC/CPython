#Calculating area of triagle
#Setting variable area to the value of 0
area = 0
#Setting the variable height to the value of 10
height= 10
#Setting the variable width to the value of 20
width = 20
#Calculate the area of the triagle
area = height*width/2
#Printing the area of the triagle, %d means decimal
print ('The area of the triagle is %d' % area)
#Does the same thing as the above 
print ('The area of the triagle is {0:f}'.format(area))
#To format and manipulate multiple numbers
print ('The first number is {0:d}, the second number is {1:d} and the third number is {2:d}'.format (1,2,3))